public class relational_opr {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        System.out.println("a == b is: "+(a==b));
        System.out.println("a != b is: "+(a!=b));
        System.out.println("a > b is: "+(a>b));
        System.out.println("a < b is: "+(a<b));
        System.out.println("b >= a is: "+(b>=a));
        System.out.println("b <= a is: "+(b<=a));
    }
}
